# operatory arytmetyczne

# TYP CAŁKOWITOLICZBOWY
# operator dodawania
print(3 + 4)

# operator odejmowania
print(4 - 2)

# operator mnożenia
print(2 * 5)

# operator dzielenia
print(6 / 2)

# operator dzielenie całkowitoliczbowe
print(5 // 2)

# operator reszty z dzieleni
print(5 % 2)

# operator potęgowania
print(3 ** 2)

# TYP ZMIENNOPRZECINKOWY
# Identycznie z operatormi arytemtycznymi dla float
print(6.2 // 3.4)
print(6.2 % 3.4)
print(0.1+0.1+0.1)

# TYP NAPISOWY
# operator kontkatenacji
print("ala"+"kota")

