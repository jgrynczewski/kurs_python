# LITERAŁY
# całkowitoliczbowy (integer)
print(3)
print(10)
print(-55)

# zmiennoprzecinkowe (float)
print(2.3)
print(5.3412)
print(4.)
print(.23)
print(3.2e3)

# napisowe (string)
print("ala ma kota")
print("Napis pierwsz")
print('Drugi napis')
print("""Trzeci napis""")

# logiczne (boolean)
print(False)
print(True)

# pusty (none)
print(None)


# Funkcja type
print(type(3))
print(type("ala ma kota"))
